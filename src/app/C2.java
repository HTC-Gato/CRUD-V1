package app;

public class C2 {
String curso;
int anoConclusao;

public C2(String curso, int anoConclusao) {
    this.curso = curso;
    this.anoConclusao = anoConclusao;
}

public String getCurso() {
	return curso;
}
public void setCurso(String curso) {
	this.curso = curso;
}
public int getAnoConclusao() {
	return anoConclusao;
}
public void setAnoConclusao(int anoConclusao) {
	this.anoConclusao = anoConclusao;
}

}
